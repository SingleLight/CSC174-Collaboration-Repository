<h2>Tom Odell</h2>

<p>Thomas Peter Odell (born 24 November 1990) is an English singer-songwriter. He released his debut extended play, Songs from Another Love, in 2012 and won the BRITs Critics' Choice Award in early 2013. His debut studio album, Long Way Down, was issued on 24 June 2013. His second studio album, Wrong Crowd, was released on 10 June 2016. In October 2018, his third studio album, Jubilee Road was released.

Odell grew up listening to Elton John. One of the first albums he listened to was Goodbye Yellow Brick Road (1973). He also cites David Bowie, Jeff Buckley, Bob Dylan, Arthur Russell, Leonard Cohen, Leon Russell, Billy Joel, Randy Newman, Tom Waits, Rodríguez and Bruce Springsteen as influences.</p>

