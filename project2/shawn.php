<h2>Coldplay</h2>

<p>Coldplay are a British rock band formed in London in 1996. The four members, lead singer and pianist Chris Martin, lead guitarist Jonny Buckland, bassist Guy Berryman, and drummer Will Champion were at University College London, and came together from 1996 to 1998, during which time the band changed names from Pectoralz, to Starfish, then Coldplay. Creative director and former manager Phil Harvey is often referred to as the fifth member by the band.

Coldplay have won numerous awards throughout their career, including nine Brit Awards, six MTV Video Music Awards, seven MTV Europe Music Awards and seven Grammy Awards from 29 nominations. They have sold more than 100 million records worldwide, making them one of the world's best-selling music artists. Three of their albums: Parachutes, A Rush of Blood to the Head, and X&Y, are among the best-selling albums in UK chart history.</p>


