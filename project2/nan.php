<h2>Common Question about these creatures</h2>
<h3> How to keep rodents away from the attic?</h3>

<p>As useful as the attic in our home is, it can also be a pest magnet. The darkness and protection against weather are just too tempting for rodents to pass by. Unfortunately, with rodents come other problems that can damage your property and even lower its value. If the material damage to your home is not enough of an incentive to try your best to keep these critters out of your house then you may also want to consider the damage to your health and that of your family and pets. Rodents can carry several diseases that can be even life threatening to you or to your furry best friend; so keeping mice, rats and other rodents out of your house should be a priority.</p>

<h3>How Do You Keep Them Out?</h3>

<p>The first thing to remember is that your home can be invaded at any time of the year. They can also fit through some of the smallest holes in your walls and the roof; this means looking for the tiny spaces where a rodent can make his way into your house or attic is the first step to keeping them out. Once you find a hole or crack you must seal it immediately. Also remember that rodent teeth are very sharp and that they can chew trough wood; so if there is already a hole on your wall or your roof then you may want to try to plug it up with metal or something that rodent’s teeth may have a hard time getting through.</p>

<h3>How to make Your Attic “Not Rodent Friendly” ?</h3>
<p>There are also some things you can do in your attic that will make it more unsuitable for rodents. Number one in that list is to keep it organized. Make sure that you organize it in a way that it is difficult for rodents to make a nest or hide. There is also natural rodent repellent, like for example mint plants. Mice are not fond of the smell of mint plants and will do anything to avoid it. Placing traps in different places is also a good idea but the only problem with that is that you’ll need to check them constantly to make sure you keep your attic clean of bodies and free of smells. You also have the choice to place poison but if you have household pets you have to make sure that they do not find it. The poison must be easy to find for the rodent but impossible to find for your dog or cat.</p>

<h3>Why It’s Important To Keep Them Out In The First Place?</h3>
<p>You do not want rodents getting into your house for numerous reasons. Following preventative measures can help. But what if rodents are in your house already? You can end up with an infestation in a brief period of time. Rodents reproduce easily and where there are two there will be more in no time. Because of the fast reproduction of rodents, the damage caused to your house can be a lot bigger than just a hole in the wall. Rodents can chew through wood or important wires. They can also leave a lot of droppings throughout your house and that can make your family or pets ill. Additionally, they die in your house and if you do not know where they are you will have a nasty smell throughout a big section of your house for a long time.</p>


