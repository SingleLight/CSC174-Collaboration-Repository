<h2>Description of the animals</h2>

<h3>Description of Raccoon</h3>

<p>One of the most commonly dealt with nuisance animal species here in central Florida. They are very capable and adaptable creatures, and I consider them very intelligent and talented. They have very nimble hands, they are very strong, and they are excellent climbers. For these reasons, they commonly break into houses, where they like to live, raise young, and steal food. Many raccoons have no fear of people (or dogs), and thus commonly come into conflict with humans in urban settings.</p>

<h3>Description of squirrels</h3>
<p>Possibly the "classic" and most common nuisance species throughout North America, and Florida is no exception. The Eastern Gray Squirrel is incredibly agile and simply loves to live inside the attics of people's homes, and will chew its way inside. Squirrels have two litters of young per year, and prefer to raise them in a safe place, such as your attic. Once inside, these rodents, like all rodents, will often chew on wood and wires, not to mention the noise they make running around up there.</p>

<h3>Description of Rats</h3>

<p>Rats are probably the single most commonly dealt with nuisance animal here in Florida. The Roof Rat (rattus rattus), also known as the Citrus Rat here in FL, invades any home with any small opening leading inside. They usually enter merely for warmth and shelter, but also for food. Once inside, they make a lot of scurrying and scratching noises, but they also chew on wires, defecate and urinate everywhere, and often enter the home and raid the pantry. They are nocturnal, and amazingly fast and agile.</p>

